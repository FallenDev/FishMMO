namespace MeshyAI
{
	/// <summary>
	/// Specifies the symmetry mode for Meshy 3D generation.
	/// </summary>
	public enum SymmetryMode
	{
		/// <summary>
		/// No symmetry applied.
		/// </summary>
		off,
		/// <summary>
		/// Symmetry is automatically determined.
		/// </summary>
		auto,
		/// <summary>
		/// Symmetry is always applied.
		/// </summary>
		on
	}
}